typedef struct {
    int char_x;
    int char_y;
} CHAR_DATA;

#include "render.h"

template <typename T>
CHAR_DATA get_ascii(T c)
{
    CHAR_DATA chr;
    chr.char_x = c % 16; chr.char_x = chr.char_x * TILE_WIDTH;
    chr.char_y = c / 16; chr.char_y = chr.char_y * TILE_HEIGHT;
    return chr;
}

